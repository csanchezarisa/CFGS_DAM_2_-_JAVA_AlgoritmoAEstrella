
public class Ciutats {

	//variables
    int Cost;
    String Ciutat;
    String Cami;
    
  //Constructor
    public Ciutats() {
    }

    //Getters i Setters
    public int getCost() {
        return Cost;
    }

    public void setCost(int Coste) {
        this.Cost = Coste;
    }
    public String getCiutat() {
        return Ciutat;
    }

    public void setCiutat(String Ciutat) {
        this.Ciutat = Ciutat;
    }
    public String getCami() {
        return Cami;
    }

    public void setCami(String Cami) {
        this.Cami= Cami;
    }	
}
